use std::sync::Arc;

use cxx::UniquePtr;

mod ffi;

#[derive(Clone)]
pub struct PerspectiveServer {
    server: Arc<UniquePtr<ffi::ProtoApiServer>>,
}

impl Default for PerspectiveServer {
    fn default() -> Self {
        let server = Arc::new(ffi::new_proto_server());
        Self { server }
    }
}

impl PerspectiveServer {
    pub fn new() -> Self {
        Self::default()
    }

    pub fn handle_message(
        &self,
        client_id: u32,
        val: &Vec<u8>,
    ) -> impl Iterator<Item = (u32, Vec<u8>)> {
        let response_batch = ffi::handle_message(&self.server, client_id, val);
        response_batch.0.into_iter().map(|x| (x.client_id, x.resp))
    }

    pub fn poll(&self) -> impl Iterator<Item = (u32, Vec<u8>)> {
        let response_batch = ffi::poll(&self.server);
        response_batch.0.into_iter().map(|x| (x.client_id, x.resp))
    }
}
